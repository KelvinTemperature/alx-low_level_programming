#include <stdio.h>
#include "dog.h"

/**
 * new_dog - creates a new dog
 * @name: name of dog
 * @age: age of dog
 * @owner: owner of dog
 */

dog_t *new_dog(char *name, float age, char *owner)
{
	name = name;
	age = age;
	owner = owner;

	if (*new_dog == NULL)
		return (NULL);
	return (new_dog);
}
