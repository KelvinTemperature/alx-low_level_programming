#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: Addition
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substrate two integers
 * @a: First
 * @b: second
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two numbers
 * @a: first
 * @b: second
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two numbers
 * @a: first
 * @b: second
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of a division
 * @a: first
 * @b: second
 * Return: Modulos
 */

int op_mod(int a, int b)
{
	return (a % b);
}
